//while loop
//while loop will check the condition, if the condition is true then it will start to execute

var a = 10;
var b = 20;

while(a <= b){
    console.log(a);
    a++;
}

//do while loop
//the difference between do wile and while loop is 
//do while loop execute the scenario at least once and will stop the process


var c = 10;
var d = 10;

do{
    console.log(c);
    c++
}while(c <= d);

